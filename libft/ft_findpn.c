int	ft_findpn(int num)
{
	int	i;

	i = 2;
	if (num <= 1)
		return (0);
	if (num == 2)
		return (1);
	while (i < num)
	{
		if (num % i == 0)
			return (0);
		i++;
	}
	return (1);
}
