char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int o;

	i = 0;
	o = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[o] != '\0')
	{
		s1[i] = s2[o];
		i++;
		o++;
	}
	s1[i] = '\0';
	return (s1);
}
