#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t o;

	i = 0;
	o = 0;
	while (s1[i] != '\0')
		i++;
	while ((s2[o] != '\0') && (o < n))
	{
		s1[i] = s2[o];
		i++;
		o++;
	}
	s1[i] = '\0';
	return (s1);
}
